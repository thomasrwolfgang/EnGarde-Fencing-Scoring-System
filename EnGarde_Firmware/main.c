/**@brief Function for handling the EnGarde Service Service events.
 *
 * @details This function will be called for all EnGarde Service events which are passed to
 *          the application.
 *
 * @param[in]   p_egs_service  EnGarde Service structure.
 * @param[in]   p_evt          Event received from the EnGarde Service.
 *
 */
static void on_egs_evt(ble_egs_t * p_egs_service, ble_egs_evt_t * p_evt) 
{

    switch(p_evt->evt_type) 
    {
        case BLE_EGS_EVT_DISCONNECTED:
            break;
        
        case BLE_EGS_EVT_CONNECTED:
            break;
        
        case BLE_EGS_EVT_NOTIFICATION_ENABLED:
            break;

        case BLE_EGS_EVT_NOTIFICATION_DISABLED:
            break;

        case BLE_EGS_EVT_HITDETECTED:
            break;

        default:
            // No implementation needed
            break;
    }
}


/**@brief Function for initializing services that will be used by the application.
 */
static void services_init(void)
{
    uint32_t           err_code;
    nrf_ble_qwr_init_t qwr_init = {0};

    // Initialize Queued Write Module.
    qwr_init.error_handler = nrf_qwr_error_handler;

    err_code = nrf_ble_qwr_init(&m_qwr, &qwr_init);
    APP_ERROR_CHECK(err_code);

    //Initialize EnGarde Service
    ble_egs_init_t      egs_init;

    memset(&egs_init, 0, sizeof(egs_init));

    egs_init.evt_handler = on_egs_evt;

    BLE_GAP_CONN_SEC_MODE_SET_OPEN(&egs_init.hitstatus_value_char_attr_md.read_perm);
    BLE_GAP_CONN_SEC_MODE_SET_OPEN(&egs_init.hitstatus_value_char_attr_md.write_perm);

    BLE_GAP_CONN_SEC_MODE_SET_OPEN(&egs_init.hittime_value_char_attr_md.read_perm);
    BLE_GAP_CONN_SEC_MODE_SET_OPEN(&egs_init.hittime_value_char_attr_md.write_perm);

    err_code = ble_egs_init(&m_egs, &egs_init);
    APP_ERROR_CHECK(err_code);
}


/**@brief Function for handling an event from the Connection Parameters Module.
 *
 * @details This function will be called for all events in the Connection Parameters Module
 *          which are passed to the application.
 *
 * @note All this function does is to disconnect. This could have been done by simply setting
 *       the disconnect_on_fail config parameter, but instead we use the event handler
 *       mechanism to demonstrate its use.
 *
 * @param[in] p_evt  Event received from the Connection Parameters Module.
 */
static void on_conn_params_evt(ble_conn_params_evt_t * p_evt)
{
    uint32_t err_code;

    if (p_evt->evt_type == BLE_CONN_PARAMS_EVT_FAILED)
    {
        err_code = sd_ble_gap_disconnect(m_conn_handle, BLE_HCI_CONN_INTERVAL_UNACCEPTABLE);
        APP_ERROR_CHECK(err_code);
    }
}


